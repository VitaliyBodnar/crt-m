const BASE_URL = 'https://block-watch.onrender.com/api/v1';
// const BASE_URL = 'http://192.168.0.143:8000/api/v1';

@Injectable({
  providedIn: 'root',
})
export class TransactionsApiService {
  readonly http = inject(HttpClient);

  getTransactions(amount: number): Observable<ISwapTransaction[]> {
    return this.http.get<any>(`${BASE_URL}/swaps/swaps/`, {
      params: {
        usdGTE: amount,
      },
    });
  }

  getTransactionsGroup(amount: number): Observable<ISwapTransactionsGroup[]> {
    return this.http.get<any>(`${BASE_URL}/swaps/swaps/group/`, {
      params: {
        usdGTE: amount,
      },
    });
  }
}
